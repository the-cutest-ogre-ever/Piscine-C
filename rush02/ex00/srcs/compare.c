#include "../includes/rush02_utils.h"

int		compare(char *str1, char *str2)
{
	int i;

	i = 0;
	while ((str1[i] != '\0') && (str2[i] != '\0') &&
			(str1[i] == str2[i]))
		i++;
	if ((str1[i] == '\0') && (str2[i] == '\0'))
		return (0);
	else
		return (-1);
}
