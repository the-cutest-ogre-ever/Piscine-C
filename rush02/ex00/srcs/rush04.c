#include "../includes/rush02_utils.h"

char	*rush04(int x, int y)
{
	char	*out;
	int		h;
	int		l;
	int		i;

	i = 0;
	h = 1;
	out = (malloc)(sizeof(char) * (y * (x + 1) + 1));
	while (h <= y && (l = 1 || 1))
	{
		while (l <= x)
		{
			(((h == 1) && (l == 1)) || ((h == y) && (h != 1) &&
				(l != 1) && (l == x))) ? (out[i++] = 'A') : 0;
			(((h != 1) && (h == y) && (l == 1)) || ((h == 1) &&
				(l != 1) && (l == x))) ? out[i++] = 'C' : 0;
			(((h == 1) || (h == y)) && (l > 1) && (l < x)) ||
				(((l == 1) || (l == x)) && h > 1 && h < y) ? out[i++] = 'B' : 0;
			((h > 1) && (h < y) && (l > 1) && (l < x)) ? out[i++] = ' ' : 0;
			(l++ == x) ? out[i++] = '\n' : 0;
		}
		h++;
	}
	out[i] = '\0';
	return (out);
}
