int	ft_is_sort(int *tab, int lenght, int (*f)(int, int))
{
	int index;

	index = 0;
	while (index < (lenght - 1))
	{
		if ((*f)(tab[index], tab[index + 1]) > 0)
			return (0);
		index++;
	}
	return (1);
}
